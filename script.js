///burger menu

 let navigataionBar = document.querySelector('.header_ul');
let navigationToggle = document.querySelector('.burger_menu');
navigationToggle.addEventListener('click', function ()
{
navigataionBar.classList.toggle('navbaractive')
});



$('#recipeCarousel').carousel({
    interval: 10000
  })
  
  $('.carousel .carousel-item').each(function(){
      var minPerSlide = 3;
      var next = $(this).next();
      if (!next.length) {
      next = $(this).siblings(':first');
      }
      next.children(':first-child').clone().appendTo($(this));
      
      for (var i=0;i<minPerSlide;i++) {
          next=next.next();
          if (!next.length) {
              next = $(this).siblings(':first');
            }
          
          next.children(':first-child').clone().appendTo($(this));
        }
  });